int	ft_isalpha(int c)
{
	int is_alpha;
	is_alpha = 1;

	if(!(c >= 65 && c <= 90))
	{
		if(!(c >= 97 && c <= 122))
		{
			is_alpha = 0;
		}
	}
	return is_alpha;
}
