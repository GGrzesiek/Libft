int	ft_isalnum(int c)
{
	int is_alnum;
	is_alnum = 1;

	if(!(c >= 65 && c <= 90))
	{
		if(!(c >= 97 && c <= 122))
		{
			if(!(c >= 48 && c <= 57))
			{
				is_alnum = 0;
			}
		}
	}

	return is_alnum;
}